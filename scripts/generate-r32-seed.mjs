// Generates supabase/seed-r32.sql — an idempotent upsert of just the 16
// Round of 32 matches (match 73–88) from scripts/fixtures.json.
//
// Unlike scripts/generate-seed.mjs (which TRUNCATEs teams/matches and reseeds
// everything), this is safe to run against a live database mid-tournament:
// it only touches the R32 rows, matches on match_number, and leaves group-stage
// matches, results, and predictions untouched.
//
// Re-run after editing fixtures.json:  npm run seed:r32
// Then paste the generated supabase/seed-r32.sql into the Supabase SQL editor.
import { readFileSync, writeFileSync } from "node:fs";

const fx = JSON.parse(
  readFileSync(new URL("./fixtures.json", import.meta.url), "utf8")
);

const esc = (s) => String(s).replace(/'/g, "''");
const nullable = (v) => (v == null ? "null" : `'${esc(v)}'`);

// Same deterministic UUID schemes as generate-seed.mjs, so a full reseed and an
// R32-only upsert produce identical ids for the same match/team.
const matchUuid = (n) =>
  `00000000-1111-4000-8000-${n.toString(16).padStart(12, "0")}`;
const teamUuid = (i) =>
  `00000000-0000-4000-8000-${(i + 1).toString(16).padStart(12, "0")}`;

// name → uuid, matching the ordering generate-seed.mjs uses for teams.
const teamId = new Map();
fx.teams.forEach((t, i) => teamId.set(t.name, teamUuid(i)));
const teamRef = (name) => {
  if (name == null) return "null";
  if (!teamId.has(name)) throw new Error(`Unknown team: ${name}`);
  return `'${teamId.get(name)}'`;
};

const r32 = fx.matches
  .filter((m) => m.stage === "r32")
  .sort((a, b) => a.match_number - b.match_number);

if (r32.length !== 16) {
  throw new Error(`Expected 16 R32 matches, found ${r32.length}.`);
}

const out = [];
out.push("-- AUTO-GENERATED by scripts/generate-r32-seed.mjs from scripts/fixtures.json");
out.push("-- Round of 32 (match 73–88) only. Kickoffs are UTC; the app renders them");
out.push("-- in Maldives time (UTC+5). Safe to run on the live DB: upserts by");
out.push("-- match_number and never touches scores, status, or predictions.");
out.push("-- Teams reference public.teams by the same deterministic ids as seed.sql.");
out.push("-- Re-run: npm run seed:r32");
out.push("");
out.push("begin;");
out.push("");
out.push(
  "insert into public.matches (id, match_number, stage, group_name, home_team_id, away_team_id, home_label, away_label, kickoff_time, venue) values"
);
out.push(
  r32
    .map(
      (m) =>
        `  ('${matchUuid(m.match_number)}', ${m.match_number}, '${m.stage}', ${nullable(
          m.group
        )}, ${teamRef(m.home)}, ${teamRef(m.away)}, ${nullable(m.home_label)}, ${nullable(
          m.away_label
        )}, '${m.kickoff_utc}', ${nullable(m.venue)})`
    )
    .join(",\n")
);
out.push("on conflict (match_number) do update set");
out.push("  stage        = excluded.stage,");
out.push("  group_name   = excluded.group_name,");
out.push("  home_team_id = excluded.home_team_id,");
out.push("  away_team_id = excluded.away_team_id,");
out.push("  home_label   = excluded.home_label,");
out.push("  away_label   = excluded.away_label,");
out.push("  kickoff_time = excluded.kickoff_time,");
out.push("  venue        = excluded.venue;");
out.push("");
out.push("commit;");
out.push("");

writeFileSync(new URL("../supabase/seed-r32.sql", import.meta.url), out.join("\n"));
console.log(`Wrote supabase/seed-r32.sql — ${r32.length} Round of 32 matches.`);
